void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int n;
	int swap;

	i = 0;
	n = size - 1;
	while (i < (size / 2))
	{
		swap = tab[i];
		tab[i] = tab[n];
		tab[n] = swap;
		i++;
		n--;
	}
}
